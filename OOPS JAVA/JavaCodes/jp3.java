//pre and post increment
// import java.lang.*;
class PrePost{
      public static void main(String args[])
       {
        int x=1;
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x);

        int y=1;
        System.out.println(y);
        System.out.println(y++);
        System.out.println(y);

        }
}