//BITWISE OPERATORS
// import java.lang.*;
class Bits{
      public static void main(String args[])
       {
        byte x=1, y=2;
        System.out.println("~X= "+ (~x));
        System.out.println("x & y= "+ (x&y));
        System.out.println("x | y= "+ (x|y));
        System.out.println("x ^ y= "+ (x^y));
        System.out.println("x << y= "+ (x<<2));
        System.out.println("x >> y= "+ (x>>2));
        System.out.println("x >>> y= "+ (x>>>2));
        }
}
